function ValidationMessage({ messages, type = 'error' }) {
  if (!messages || messages.length === 0) {
    return null;
  }

  const styles = {
    error: {
      container: 'bg-red-50 border-l-4 border-red-500 p-4 mb-4',
      icon: 'text-red-500',
      text: 'text-red-700'
    },
    warning: {
      container: 'bg-yellow-50 border-l-4 border-yellow-500 p-4 mb-4',
      icon: 'text-yellow-500',
      text: 'text-yellow-700'
    },
    info: {
      container: 'bg-blue-50 border-l-4 border-blue-500 p-4 mb-4',
      icon: 'text-blue-500',
      text: 'text-blue-700'
    }
  };

  const style = styles[type] || styles.error;

  return (
    <div className={style.container}>
      <div className="flex">
        <div className="flex-shrink-0">
          {type === 'error' && (
            <svg className={`h-5 w-5 ${style.icon}`} fill="currentColor" viewBox="0 0 20 20">
              <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z" clipRule="evenodd" />
            </svg>
          )}
          {type === 'warning' && (
            <svg className={`h-5 w-5 ${style.icon}`} fill="currentColor" viewBox="0 0 20 20">
              <path fillRule="evenodd" d="M8.257 3.099c.765-1.36 2.722-1.36 3.486 0l5.58 9.92c.75 1.334-.213 2.98-1.742 2.98H4.42c-1.53 0-2.493-1.646-1.743-2.98l5.58-9.92zM11 13a1 1 0 11-2 0 1 1 0 012 0zm-1-8a1 1 0 00-1 1v3a1 1 0 002 0V6a1 1 0 00-1-1z" clipRule="evenodd" />
            </svg>
          )}
          {type === 'info' && (
            <svg className={`h-5 w-5 ${style.icon}`} fill="currentColor" viewBox="0 0 20 20">
              <path fillRule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z" clipRule="evenodd" />
            </svg>
          )}
        </div>
        <div className="ml-3">
          {messages.length === 1 ? (
            <p className={`text-sm font-medium ${style.text}`}>{messages[0]}</p>
          ) : (
            <ul className={`text-sm ${style.text} list-disc list-inside space-y-1`}>
              {messages.map((message, index) => (
                <li key={index}>{message}</li>
              ))}
            </ul>
          )}
        </div>
      </div>
    </div>
  );
}

export default ValidationMessage;
